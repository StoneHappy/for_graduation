	void createRCGraphicsPieline()
	{
        VkShaderModule vertexShader = createShader(GLOBAL_VULKAN_CONTEXT->logicalDevice, navmesh_vert, sizeof(navmesh_vert));
        VkShaderModule fragShader = createShader(GLOBAL_VULKAN_CONTEXT->logicalDevice, navmesh_frag, sizeof(navmesh_frag));
        std::vector<VkPipelineShaderStageCreateInfo> shaderStage;
        createShaderStageInfo(vertexShader, fragShader, shaderStage);
        // descript binding point
        createRCDescriptorSetLayout(GLOBAL_VULKAN_CONTEXT->rcDescriptorSetLayout);
        // create graphicspipeline according to descriptor and qt default pipelinelayout
        createRCPipelineLayout(GLOBAL_VULKAN_CONTEXT->rcDescriptorSetLayout, GLOBAL_VULKAN_CONTEXT->rcPipelineLayout);
        // create graphicspipeline according to layout
        createRCGraphicsPipeline(shaderStage, GLOBAL_VULKAN_CONTEXT->rcPipelineLayout, GLOBAL_VULKAN_CONTEXT->rcPipeline);
        createRCContourPipeline(shaderStage, GLOBAL_VULKAN_CONTEXT->rcPipelineLayout, GLOBAL_VULKAN_CONTEXT->rcContourPipeline);

        createRCDescriptorSets(GLOBAL_VULKAN_CONTEXT->rcDescriptorSetLayout, GLOBAL_VULKAN_CONTEXT->descriptorPool, GLOBAL_VULKAN_CONTEXT->rcDescriptorSets);
	}
